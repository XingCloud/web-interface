package com.xingcloud.webinterface.exception;

public class UserPropertyException extends Exception {

  /**
   *
   */
  private static final long serialVersionUID = -3386205609347796010L;

  public UserPropertyException() {
    super();
  }

  public UserPropertyException(String message, Throwable cause) {
    super(message, cause);
  }

  public UserPropertyException(String message) {
    super(message);
  }

  public UserPropertyException(Throwable cause) {
    super(cause);
  }

}

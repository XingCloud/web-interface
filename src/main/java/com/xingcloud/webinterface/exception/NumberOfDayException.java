package com.xingcloud.webinterface.exception;

public class NumberOfDayException extends Exception {

  private static final long serialVersionUID = -6506919070752632102L;

  public NumberOfDayException() {
    super();
  }

  public NumberOfDayException(String message, Throwable cause) {
    super(message, cause);
  }

  public NumberOfDayException(String message) {
    super(message);
  }

  public NumberOfDayException(Throwable cause) {
    super(cause);
  }

}

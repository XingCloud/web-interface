package com.xingcloud.webinterface.exception;

public class RangingException extends Exception {

  private static final long serialVersionUID = -247286357219770464L;

  public RangingException() {
    super();
  }

  public RangingException(String message, Throwable cause) {
    super(message, cause);
  }

  public RangingException(String message) {
    super(message);
  }

  public RangingException(Throwable cause) {
    super(cause);
  }

}

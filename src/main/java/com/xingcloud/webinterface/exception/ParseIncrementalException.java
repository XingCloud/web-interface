package com.xingcloud.webinterface.exception;

public class ParseIncrementalException extends Exception {

  private static final long serialVersionUID = 2246759034440000634L;

  public ParseIncrementalException() {
    super();
  }

  public ParseIncrementalException(String message, Throwable cause) {
    super(message, cause);
  }

  public ParseIncrementalException(String message) {
    super(message);
  }

  public ParseIncrementalException(Throwable cause) {
    super(cause);
  }

}
